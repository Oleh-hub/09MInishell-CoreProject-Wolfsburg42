#include "minishell.h"

/* 
roi 0611
	if the string contains only digits, return 1, otherwise 0
 */
int	ft_has_only_digit(char *str)
{
	while (*str)
	{
		if (!ft_isdigit(*str))
			return (0);
		str++;
	}
	return (1);
}
